package com.imin.vo;

import java.util.List;

import com.imin.domain.Book;
/**
 * @Description: 用于封装首页需要的所有数据
 * @Version:1.0
 * @author nick
 * @date 2018年9月22日
 */
public class IndexVo {

	//新书上架
	private List<Book> newest;
	//热门推荐
	private List<Book> recommend;
	//热销排行
	private List<Book> hotSales;
	//今日特价
	private List<Book> discount;
	
	public IndexVo() {
		super();
	}
	public List<Book> getNewest() {
		return newest;
	}
	public void setNewest(List<Book> newest) {
		this.newest = newest;
	}
	public List<Book> getRecommend() {
		return recommend;
	}
	public void setRecommend(List<Book> recommend) {
		this.recommend = recommend;
	}
	public List<Book> getHotSales() {
		return hotSales;
	}
	public void setHotSales(List<Book> hotSales) {
		this.hotSales = hotSales;
	}
	public List<Book> getDiscount() {
		return discount;
	}
	public void setDiscount(List<Book> discount) {
		this.discount = discount;
	}

}
